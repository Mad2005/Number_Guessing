    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       try {
        int userGuess = Integer.parseInt(guessing.getText());

        if (userGuess < 1 || userGuess > 100) {
            JOptionPane.showMessageDialog(this, "Please enter a number between 1 and 100.");
            return;
        }

        attemptsLeft--;
        updateAttemptsTable();

        if (userGuess == numberToGuess) {
            JOptionPane.showMessageDialog(this, String.format("Hurray! Number Guessed Successfully. Attempts Left = %d.", attemptsLeft));
            totalScore += (attemptsLeft + 1) * 10; // Points based on remaining attempts
            updateScoreTable();

            if (currentRound < rounds) {
                currentRound++;
                generateNewNumber();
                updateRoundsTable();
            } else {
                JOptionPane.showMessageDialog(this, "Game Over! Your final score is: " + totalScore);
                initializeGame();
            }
        } else {
            String hint = userGuess < numberToGuess ? "higher" : "lower";
            JOptionPane.showMessageDialog(this, "The number is " + hint + " than " + userGuess + ". Attempts Left = " + attemptsLeft);
            if (attemptsLeft <= 0) {
                JOptionPane.showMessageDialog(this, "Sorry, you've used all attempts. The number was " + numberToGuess);
                if (currentRound < rounds) {
                    currentRound++;
                    generateNewNumber();
                    updateRoundsTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Game Over! Your final score is: " + totalScore);
                    initializeGame();
                }
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid input. Please enter a valid number.");
    }



    }//GEN-LAST:event_jButton2ActionPerformed

