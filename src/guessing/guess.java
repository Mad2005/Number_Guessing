private void initTableModels() {
    roundsTableModel = new DefaultTableModel(new Object[][]{{0}}, new String[]{"Rounds Left"});
    attemptsTableModel = new DefaultTableModel(new Object[][]{{0}}, new String[]{"Attempts Left"});
    scoreTableModel = new DefaultTableModel(new Object[][]{{0}}, new String[]{"Score"});

    r.setModel(roundsTableModel);
    a.setModel(attemptsTableModel);
    s.setModel(scoreTableModel);
}

    private void initializeGame() {
        // Initialize game variables
        rounds = 3;
        currentRound = 1;
        totalScore = 0;
        attemptsLeft = 8;
        generateNewNumber();
    }
    
    private void updateRoundsTable() {
    roundsTableModel.setValueAt(rounds - currentRound + 1, 0, 0);
}

private void updateAttemptsTable() {
    attemptsTableModel.setValueAt(attemptsLeft, 0, 0);
}

private void updateScoreTable() {
    scoreTableModel.setValueAt(totalScore, 0, 0);
}


    private void generateNewNumber() {
        Random random = new Random();
        numberToGuess = random.nextInt(100) + 1; // Random number between 1 and 100
        attemptsLeft = 8; // Reset attempts for each round
    }
